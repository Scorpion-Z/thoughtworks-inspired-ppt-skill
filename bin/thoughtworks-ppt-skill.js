#!/usr/bin/env node
import fs from 'fs';
import os from 'os';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const root = path.resolve(__dirname, '..');
const skillName = 'boge-ppt-skill';
const defaultLocalSkillDir = path.join(os.homedir(), '.codex', 'skills', skillName);

function exists(relativePath) {
  return fs.existsSync(path.join(root, relativePath));
}

function copyRecursive(src, dest) {
  const stat = fs.statSync(src);
  if (stat.isDirectory()) {
    fs.mkdirSync(dest, { recursive: true });
    for (const item of fs.readdirSync(src)) {
      copyRecursive(path.join(src, item), path.join(dest, item));
    }
  } else {
    fs.mkdirSync(path.dirname(dest), { recursive: true });
    fs.copyFileSync(src, dest);
  }
}

function copyIfPresent(relativePath, out) {
  if (exists(relativePath)) {
    copyRecursive(path.join(root, relativePath), path.join(out, relativePath));
  }
}

function writeGeneratedReadme(out, mode) {
  const readme = mode === 'skill'
    ? `# Boge PPT Skill\n\nThis folder is installed as an agent skill.\n\nAsk Codex to read \`SKILL.md\` first, then use \`templates/\`, \`styles/\`, \`assets/\`, \`scripts/\`, and \`references/\` to generate a Boge consulting-style HTML presentation.\n\nThis project is an unofficial style-inspired skill. It does not include Thoughtworks official logos or proprietary assets.\n`
    : `# Generated Boge PPT Deck\n\nOpen \`index.html\` in a browser.\n\nThis deck is generated by boge-ppt-skill. It is an unofficial style-inspired template.\n`;
  fs.writeFileSync(path.join(out, 'README.md'), readme);
}

function installSkill(target = defaultLocalSkillDir) {
  const out = path.resolve(process.cwd(), target);
  fs.mkdirSync(out, { recursive: true });
  copyRecursive(path.join(root, 'SKILL.md'), path.join(out, 'SKILL.md'));
  copyIfPresent('templates', out);
  copyIfPresent('styles', out);
  copyIfPresent('assets', out);
  copyIfPresent('scripts', out);
  copyIfPresent('examples', out);
  copyIfPresent('references', out);
  writeGeneratedReadme(out, 'skill');
  console.log(`Installed ${skillName} at ${out}`);
  console.log('Use it in Codex: 请读取 ~/.codex/skills/boge-ppt-skill/SKILL.md，并按该规范生成 HTML PPT。');
}

function writeDeckIndex(out, demoMode) {
  let html = fs.readFileSync(path.join(root, 'templates', 'index.html'), 'utf8');
  if (demoMode) {
    html = html.replace(
      '<title>[必填] 替换为 PPT 标题</title>',
      '<title>以数智化支撑集团经营穿透能力提升</title>'
    );
  }
  fs.writeFileSync(path.join(out, 'index.html'), html);
}

function initDeck(target, options = {}) {
  if (!target) {
    usage();
    process.exit(1);
  }
  const out = path.resolve(process.cwd(), target);
  fs.mkdirSync(out, { recursive: true });
  writeDeckIndex(out, Boolean(options.demoMode));
  copyIfPresent('styles', out);
  copyIfPresent('assets', out);
  copyIfPresent('references', out);
  copyIfPresent('scripts', out);
  fs.mkdirSync(path.join(out, 'images'), { recursive: true });
  copyRecursive(path.join(root, 'SKILL.md'), path.join(out, 'SKILL.md'));
  copyIfPresent('examples', out);
  writeGeneratedReadme(out, 'deck');
  console.log(`Created Boge PPT deck at ${out}`);
}

function doctor() {
  const required = [
    'SKILL.md',
    'README.md',
    'templates/index.html',
    'styles/thoughtworks-inspired.css',
    'assets/diagrams',
    'assets/vendor/motion.js',
    'references/style-lock.md',
    'references/layout-lock.md',
    'references/layouts.md',
    'references/themes.md',
    'references/components.md',
    'references/image-prompts.md',
    'references/checklist.md',
    'scripts/validate-thoughtworks-deck.mjs',
    'scripts/visual-check-deck.mjs',
    'scripts/boge-deck-runtime.js',
    'examples/full-layout-demo/index.html',
    'examples/template-suite/index.html',
    'examples/template-gallery/index.html',
  ];
  const missing = required.filter((item) => !exists(item));
  if (missing.length) {
    console.error(`Missing required paths: ${missing.join(', ')}`);
    process.exit(1);
  }
  console.log('Skill package looks complete. Required paths exist.');
}

function usage() {
  console.log(`Usage:
  npx github:Scorpion-Z/thoughtworks-inspired-ppt-skill
  npx github:Scorpion-Z/thoughtworks-inspired-ppt-skill install [target-dir]
  npx github:Scorpion-Z/thoughtworks-inspired-ppt-skill global
  npx github:Scorpion-Z/thoughtworks-inspired-ppt-skill init <target-dir> [--demo]
  npx github:Scorpion-Z/thoughtworks-inspired-ppt-skill doctor

Package bin aliases:
  boge-ppt-skill
  thoughtworks-inspired-ppt-skill
  thoughtworks-ppt-skill

  npm run check
  npm run visual:demo

Skill registry style:
  npx skills add https://github.com/Scorpion-Z/thoughtworks-inspired-ppt-skill --skill boge-ppt-skill

Default:
  Installs the skill to ~/.codex/skills/boge-ppt-skill

Examples:
  npx github:Scorpion-Z/thoughtworks-inspired-ppt-skill
  npx github:Scorpion-Z/thoughtworks-inspired-ppt-skill install .codex/skills/boge-ppt-skill
  npx github:Scorpion-Z/thoughtworks-inspired-ppt-skill global
  npx github:Scorpion-Z/thoughtworks-inspired-ppt-skill init my-deck --demo`);
}

const argv = process.argv.slice(2);
const [cmd, target] = argv;
const demoMode = argv.includes('--demo');

if (!cmd) {
  installSkill(defaultLocalSkillDir);
  process.exit(0);
}

if (cmd === '-h' || cmd === '--help' || cmd === 'help') {
  usage();
  process.exit(0);
}

if (cmd === 'install' || cmd === 'add' || cmd === 'codex') {
  installSkill(target || defaultLocalSkillDir);
  process.exit(0);
}

if (cmd === 'global') {
  installSkill(path.join(os.homedir(), '.codex', 'skills', skillName));
  process.exit(0);
}

if (cmd === 'init') {
  initDeck(target, { demoMode });
  process.exit(0);
}

if (cmd === 'doctor') {
  doctor();
  process.exit(0);
}

usage();
process.exit(1);
