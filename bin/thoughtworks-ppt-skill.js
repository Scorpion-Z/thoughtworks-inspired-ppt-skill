#!/usr/bin/env node
import fs from 'fs';
import os from 'os';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const root = path.resolve(__dirname, '..');
const skillName = 'thoughtworks-inspired-ppt-skill';
const defaultLocalSkillDir = path.join('.codex', 'skills', skillName);

function exists(relativePath) {
  return fs.existsSync(path.join(root, relativePath));
}

function copyRecursive(src, dest) {
  const stat = fs.statSync(src);
  if (stat.isDirectory()) {
    fs.mkdirSync(dest, { recursive: true });
    for (const item of fs.readdirSync(src)) {
      copyRecursive(path.join(src, item), path.join(dest, item));
    }
  } else {
    fs.mkdirSync(path.dirname(dest), { recursive: true });
    fs.copyFileSync(src, dest);
  }
}

function copyIfPresent(relativePath, out) {
  if (exists(relativePath)) {
    copyRecursive(path.join(root, relativePath), path.join(out, relativePath));
  }
}

function writeGeneratedReadme(out, mode) {
  const readme = mode === 'skill'
    ? `# Thoughtworks-inspired PPT Skill\n\nThis folder is installed as an agent skill.\n\nAsk Codex to read \`SKILL.md\` first, then use \`templates/\`, \`styles/\`, \`assets/\`, and \`references/\` to generate a Thoughtworks-inspired HTML presentation.\n\nThis project is an unofficial style-inspired skill. It does not include Thoughtworks official logos or proprietary assets.\n`
    : `# Generated Thoughtworks-inspired Deck\n\nOpen \`index.html\` in a browser.\n\nThis deck is generated by thoughtworks-inspired-ppt-skill. It is an unofficial style-inspired template.\n`;
  fs.writeFileSync(path.join(out, 'README.md'), readme);
}

function installSkill(target = defaultLocalSkillDir) {
  const out = path.resolve(process.cwd(), target);
  fs.mkdirSync(out, { recursive: true });
  copyRecursive(path.join(root, 'SKILL.md'), path.join(out, 'SKILL.md'));
  copyIfPresent('templates', out);
  copyIfPresent('styles', out);
  copyIfPresent('assets', out);
  copyIfPresent('examples', out);
  copyIfPresent('references', out);
  writeGeneratedReadme(out, 'skill');
  console.log(`Installed ${skillName} at ${out}`);
  console.log('Use it in Codex: 请读取 .codex/skills/thoughtworks-inspired-ppt-skill/SKILL.md，并按该规范生成 HTML PPT。');
}

function initDeck(target) {
  if (!target) {
    usage();
    process.exit(1);
  }
  const out = path.resolve(process.cwd(), target);
  fs.mkdirSync(out, { recursive: true });
  copyRecursive(path.join(root, 'templates', 'index.html'), path.join(out, 'index.html'));
  copyIfPresent('styles', out);
  copyIfPresent('assets', out);
  copyIfPresent('references', out);
  copyRecursive(path.join(root, 'SKILL.md'), path.join(out, 'SKILL.md'));
  copyIfPresent('examples', out);
  writeGeneratedReadme(out, 'deck');
  console.log(`Created Thoughtworks-inspired deck at ${out}`);
}

function doctor() {
  const required = ['SKILL.md', 'README.md', 'templates', 'styles', 'assets', 'references'];
  const missing = required.filter((item) => !exists(item));
  if (missing.length) {
    console.error(`Missing required paths: ${missing.join(', ')}`);
    process.exit(1);
  }
  console.log('Skill package looks complete. Required paths exist.');
}

function usage() {
  console.log(`Usage:
  npx github:Scorpion-Z/thoughtworks-inspired-ppt-skill
  npx github:Scorpion-Z/thoughtworks-inspired-ppt-skill install [target-dir]
  npx github:Scorpion-Z/thoughtworks-inspired-ppt-skill global
  npx github:Scorpion-Z/thoughtworks-inspired-ppt-skill init <target-dir> [--demo]
  npx github:Scorpion-Z/thoughtworks-inspired-ppt-skill doctor

Skill registry style:
  npx skills add https://github.com/Scorpion-Z/thoughtworks-inspired-ppt-skill --skill thoughtworks-inspired-ppt-skill

Default:
  Installs the skill to .codex/skills/thoughtworks-inspired-ppt-skill

Examples:
  npx github:Scorpion-Z/thoughtworks-inspired-ppt-skill
  npx github:Scorpion-Z/thoughtworks-inspired-ppt-skill install .codex/skills/thoughtworks-inspired-ppt-skill
  npx github:Scorpion-Z/thoughtworks-inspired-ppt-skill global
  npx github:Scorpion-Z/thoughtworks-inspired-ppt-skill init my-deck --demo`);
}

const [cmd, target] = process.argv.slice(2);

if (!cmd) {
  installSkill(defaultLocalSkillDir);
  process.exit(0);
}

if (cmd === '-h' || cmd === '--help' || cmd === 'help') {
  usage();
  process.exit(0);
}

if (cmd === 'install' || cmd === 'add' || cmd === 'codex') {
  installSkill(target || defaultLocalSkillDir);
  process.exit(0);
}

if (cmd === 'global') {
  installSkill(path.join(os.homedir(), '.codex', 'skills', skillName));
  process.exit(0);
}

if (cmd === 'init') {
  initDeck(target);
  process.exit(0);
}

if (cmd === 'doctor') {
  doctor();
  process.exit(0);
}

usage();
process.exit(1);
