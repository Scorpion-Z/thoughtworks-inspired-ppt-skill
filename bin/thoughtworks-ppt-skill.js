#!/usr/bin/env node
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const root = path.resolve(__dirname, '..');

function copyRecursive(src, dest) {
  const stat = fs.statSync(src);
  if (stat.isDirectory()) {
    fs.mkdirSync(dest, { recursive: true });
    for (const item of fs.readdirSync(src)) copyRecursive(path.join(src, item), path.join(dest, item));
  } else {
    fs.mkdirSync(path.dirname(dest), { recursive: true });
    fs.copyFileSync(src, dest);
  }
}

function usage() {
  console.log(`Usage:\n  thoughtworks-ppt-skill init <target-dir> [--demo]\n\nExamples:\n  npx thoughtworks-inspired-ppt-skill init my-deck --demo\n  npx github:Scorpion-Z/thoughtworks-inspired-ppt-skill init my-deck --demo`);
}

const [cmd, target] = process.argv.slice(2);
if (!cmd || cmd === '-h' || cmd === '--help') { usage(); process.exit(0); }
if (cmd !== 'init' || !target) { usage(); process.exit(1); }

const out = path.resolve(process.cwd(), target);
fs.mkdirSync(out, { recursive: true });
copyRecursive(path.join(root, 'templates', 'index.html'), path.join(out, 'index.html'));
copyRecursive(path.join(root, 'styles'), path.join(out, 'styles'));
copyRecursive(path.join(root, 'assets'), path.join(out, 'assets'));
copyRecursive(path.join(root, 'SKILL.md'), path.join(out, 'SKILL.md'));
copyRecursive(path.join(root, 'examples'), path.join(out, 'examples'));
fs.writeFileSync(path.join(out, 'README.md'), `# Generated Thoughtworks-inspired Deck\n\nOpen \`index.html\` in a browser.\n\nThis deck is generated by thoughtworks-inspired-ppt-skill. It is not an official Thoughtworks template.\n`);
console.log(`Created Thoughtworks-inspired deck at ${out}`);
